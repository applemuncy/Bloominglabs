# function for '1 hit' # note - should we reduce or specify the length?
def placeHit(data, sound, track, duration = 1):
  stepsize = BARS/len(data)
  i=0
  for bd in data:
    if bd:
      fitMedia(sound, track, 1+int(i*stepsize), 1+int(i*stepsize) + duration)
    i = i+ 1 

# for tracks where duration is coming into play also
def layTrack(data, sounds, track):
  stepsize = BARS/len(data)
  trackon = 0
  trackstart = 0
  # if it flips on or off, do a thing.
  for i in range(len(data)):
    if trackon and data[i] == 0: # turned off.
      fitMedia(sounds[randint(0, len(sounds) - 1)], track, 1+int(trackstart*stepsize), int(i*stepsize))
      trackon = 0
    elif not trackon and data[i]:
      trackon = 1
      trackstart = i
  
  # do a thing?
  if trackon:
    fitMedia(sounds[randint(0, len(sounds) - 1)], track, int(trackstart*stepsize), BARS)

